/**
 * Translation evaluation. Used by `evaluateTranslation`. Generated by
 * `gpt-4o` at temperature 0.4.
 *
 * Replaces the hand-rolled `validateEvaluationResponse` (deleted by 9-7).
 * The hand-rolled validator silently coerced an invalid `overallScore` to
 * `-1`; the schema rejects out-of-range values, and the caller's existing
 * recompute-from-weighted-average path handles the resulting parse retry
 * (or a missing `overallScore` after a successful retry).
 *
 * `expectedTranslation` and `userTranscription` are NOT part of the AI
 * response — they're attached by the caller after parsing — so they're
 * `.optional()` on the schema.
 */
export const translationDimensionScoreSchema = z.object({
  score: z.number().min(0).max(100),
  feedback: z.string().min(1),
});

export const translationEvaluationSchema = z.object({
  accuracy: translationDimensionScoreSchema,
  fluency: translationDimensionScoreSchema,
  naturalness: translationDimensionScoreSchema,
  // overallScore is optional — caller recomputes from weighted dimensions if
  // the model omits it or returns out-of-range. The model is asked to provide
  // it; we tolerate omission to match the existing recompute behavior.
  overallScore: z.number().min(0).max(100).optional(),
  corrections: z.string().optional(),
  // Caller-attached metadata, not validated against the AI response.
  expectedTranslation: z.string().optional(),
  userTranscription: z.string().optional(),
});

