/**
 * AI Response Schemas — Story 9-7
 *
 * Centralized Zod schemas for every `chatCompletionJSON` call site in the
 * codebase. One source of truth for "what does the AI return," consumed by:
 *
 *   - `src/lib/openai.ts` `chatCompletionJSON<T>` — validates each response
 *     before it reaches the caller. On parse failure, the call is retried
 *     once; on retry exhaustion, `captureError(_, "ai-schema-parse-failed",
 *     { feature, attempt, code })` fires and the call rejects.
 *
 *   - Future Epic 15.5 (AI schema regression tests) — replays recorded model
 *     outputs through these schemas in CI.
 *
 *   - The TypeScript layer — `z.infer<typeof X>` is the type used by every
 *     consumer. The hand-rolled interfaces in `src/types/exercise.ts` and
 *     `src/types/conversation.ts` are now re-exports of these inferred types
 *     so the type system mirrors runtime validation exactly.
 *
 * Schema rules expressed declaratively here REPLACE the four hand-rolled
 * validators (`validateMCQExercise`, `validateEchoResponse`,
 * `validateTranslationResponse`, `validateEvaluationResponse`) and the inline
 * mock-test option-filter and placement-test polymorphic-options
 * normalization. None of those manual validators remain in the codebase.
 *
 * @see docs path: this file is the contract; CLAUDE.md "AI response
 *   validation" line points here.
 */

import { z } from "zod";

/**
 * Mirrors `MAX_PRE_SANITIZE_CHARS` from `src/lib/memory.ts`. Hardcoded here
 * (rather than imported) because `memory.ts` imports this file for
 * `factExtractionSchema`, and a circular import causes the constant to be
 * `undefined` on first read in module-graph traversal — silently breaking
 * the `.max()` rule on `factSchema.content`. If the source-of-truth in
 * `memory.ts` changes, update this constant in lockstep (the two values
 * are asserted equal in `ai-responses.test.ts`).
 */
const MAX_PRE_SANITIZE_CHARS = 4096;

// ---------------------------------------------------------------------------
// Common atomic schemas — declared once, reused across feature schemas
// ---------------------------------------------------------------------------

/**
 * CEFR proficiency level. Mirrors `src/types/cefr.ts` `CEFRLevel`.
 * Hard-coded enum (not derived from `CEFR_ORDER`) because Zod's
 * `z.enum(...)` requires a literal tuple; the runtime value is identical.
 */
export const cefrLevelSchema = z.enum(["A1", "A2", "B1", "B2", "C1", "C2"]);

/**
 * MCQ option — used by listening, reading, grammar, mock-test, placement-test.
 * Required fields: `id`, `text`, `isCorrect`. `id` and `text` must be
 * non-empty strings.
 */
export const mcqOptionSchema = z.object({
  id: z.string().min(1),
  text: z.string().min(1),
  isCorrect: z.boolean(),
});

/**
 * MCQ question — exactly 4 options, exactly 1 correct, non-empty
 * explanation. The `superRefine` enforces the "exactly 1 correct" invariant
 * that previously required the hand-rolled `validateMCQExercise` function.
 */
export const mcqQuestionSchema = z
  .object({
    question: z.string().min(1),
    passage: z.string().optional(),
    passageId: z.string().optional(),
    options: z.array(mcqOptionSchema).length(4),
    explanation: z.string().min(1),
  })
  .superRefine((q, ctx) => {
    const correctCount = q.options.filter((o) => o.isCorrect).length;
    if (correctCount !== 1) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: `Expected exactly 1 correct option, got ${correctCount}`,
        path: ["options"],
      });
    }
  });

