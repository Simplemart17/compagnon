/**
 * Coerce a string|boolean truth flag to boolean. AI models occasionally emit
 * `correct: "true"` (string) instead of a boolean. The deleted
 * `resolveIsCorrect` helper handled this; restored here as a private util so
 * the schema's `.transform()` preserves the same coverage. Story 9-7 review.
 */
function coerceTruthy(v: unknown): boolean | undefined {
  if (typeof v === "boolean") return v;
  if (typeof v === "string") {
    const s = v.toLowerCase().trim();
    if (s === "true") return true;
    if (s === "false") return false;
  }
  return undefined;
}

/**
 * Placement test option — the AI sometimes returns options as a record
 * `{ a: "text", b: "text" }` instead of an array, sometimes uses
 * `correct: true` instead of `isCorrect: true`, and occasionally returns
 * `correct: "true"` as a string. The `preprocess` step on
 * `placementQuestionSchema` normalizes the question shape upstream of this
 * schema, so by the time we hit `placementOptionInputSchema` the options
 * are already an array; this schema then accepts any of the truth-flag
 * field name + type variants and an `id` or `label` for text.
 *
 * The boolean fields are typed as `unknown` so a string-encoded boolean
 * (`"true"`/`"false"`) doesn't fail validation upfront — the `.transform()`
 * runs `coerceTruthy()` to normalize.
 */
const placementOptionInputSchema = z
  .object({
    id: z.union([z.string(), z.number()]).optional(),
    label: z.string().optional(),
    text: z.string().optional(),
    value: z.string().optional(),
    isCorrect: z.unknown().optional(),
    correct: z.unknown().optional(),
    is_correct: z.unknown().optional(),
  })
  .transform((o, ctx) => {
    const idRaw = o.id ?? o.label?.toLowerCase() ?? "";
    const id = typeof idRaw === "number" ? String(idRaw) : idRaw;
    const text = o.text ?? o.label ?? o.value ?? "";
    if (!id) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: "Option missing id/label",
      });
      return z.NEVER;
    }
    if (!text) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: "Option missing text/label/value",
      });
      return z.NEVER;
    }
    const isCorrect =
      coerceTruthy(o.isCorrect) ?? coerceTruthy(o.correct) ?? coerceTruthy(o.is_correct) ?? false;
    return { id, text, isCorrect };
  });

/**
 * Placement test question — `preprocess` normalizes the AI's polymorphic
 * options shape (object-vs-array, `correct_answer` field on the question
 * vs `isCorrect` on the option) before validating. Replaces the manual
 * `resolveIsCorrect` + 30-line normalization at
 * `app/onboarding/placement-test.tsx:489-518`.
 */
/**
 * Normalize a question-level `correct_answer` (or its many aliases) to a
 * lowercase, trimmed string id. Returns `""` if no field is present.
 *
 * Handles AI quirks documented across real placement-test runs (story 9-7
 * review, finding P1):
 *   - case mismatch: `correct_answer: "B"` against options keyed `a,b,c,d`
 *   - numeric index: `correct_answer: 1` (means option at index 1)
 *   - field-name variance: `answer`, `correctAnswer`, `correct`,
 *     `correctOption`, `correct_option`
 */
function resolvePlacementCorrectKey(obj: Record<string, unknown>): {
  correctId: string;
  correctIdx: number | null;
} {
  const raw =
    obj.correct_answer ??
    obj.answer ??
    obj.correctAnswer ??
    obj.correct ??
    obj.correctOption ??
    obj.correct_option;
  if (raw === undefined || raw === null) return { correctId: "", correctIdx: null };
  if (typeof raw === "number" && Number.isInteger(raw) && raw >= 0) {
    return { correctId: "", correctIdx: raw };
  }
  return { correctId: String(raw).toLowerCase().trim(), correctIdx: null };
}

export const placementQuestionSchema = z
  .preprocess(
    (q: unknown) => {
      if (!q || typeof q !== "object") return q;
      const obj = q as Record<string, unknown>;
      const rawOpts = obj.options;

      // Convert { a: "...", b: "..." } shape → array shape
      if (rawOpts && typeof rawOpts === "object" && !Array.isArray(rawOpts)) {
        const optsArray = Object.entries(rawOpts as Record<string, unknown>)
          // Treat null/undefined values as a hard error rather than coercing
          // to the literal string "null" / "undefined". Story 9-7 review (P7).
          .filter(([, val]) => val !== null && val !== undefined)
          .map(([key, val]) => {
            if (typeof val === "string") {
              return { id: key.toLowerCase(), text: val, isCorrect: false };
            }
            if (val && typeof val === "object") {
              const v = val as Record<string, unknown>;
              return {
                id: key.toLowerCase(),
                text: typeof v.text === "string" ? v.text : String(val),
                isCorrect: coerceTruthy(v.isCorrect) ?? coerceTruthy(v.correct) ?? false,
              };
            }
            return { id: key.toLowerCase(), text: String(val), isCorrect: false };
          });
        // Resolve correct from question-level field (correct_answer/answer/etc.)
        // — both the lowercased-id and numeric-index branches.
        const { correctId, correctIdx } = resolvePlacementCorrectKey(obj);
        if (correctId) {
          const target = optsArray.find((o) => o.id === correctId);
          if (target) target.isCorrect = true;
        } else if (correctIdx !== null && correctIdx < optsArray.length) {
          optsArray[correctIdx].isCorrect = true;
        }
        return { ...obj, options: optsArray };
      }

      // Already an array — filter null/non-object entries (story 9-7 review,
      // P7) and resolve correct flags through coerceTruthy to handle string-
      // boolean variants.
      if (Array.isArray(rawOpts)) {
        const cleanOpts = rawOpts.filter((o) => o && typeof o === "object");
        const hasCorrect = cleanOpts.some((o) => {
          const oo = o as Record<string, unknown>;
          return (
            coerceTruthy(oo.isCorrect) === true ||
            coerceTruthy(oo.correct) === true ||
            coerceTruthy(oo.is_correct) === true
          );
        });
        if (!hasCorrect) {
          const { correctId, correctIdx } = resolvePlacementCorrectKey(obj);
          if (correctId || correctIdx !== null) {
            const newOpts = cleanOpts.map((o, i) => {
              const oo = o as Record<string, unknown>;
              const rawId = (oo.id as string | number | undefined) ?? oo.label;
              const oid =
                typeof rawId === "string"
                  ? rawId.toLowerCase().trim()
                  : typeof rawId === "number"
                    ? String(rawId)
                    : String.fromCharCode(97 + i);
              const matchesById = correctId !== "" && oid === correctId;
              const matchesByIdx = correctIdx !== null && i === correctIdx;
              return { ...oo, isCorrect: matchesById || matchesByIdx };
            });
            return { ...obj, options: newOpts };
          }
        }
        // Even if a `correct` field is already present, normalize array entries
        // to drop null/non-object slots so the inner schema doesn't fail on
        // sparse input.
        return { ...obj, options: cleanOpts };
      }

      return q;
    },
    z.object({
      level: cefrLevelSchema,
      question: z.string().min(1),
      options: z.array(placementOptionInputSchema).length(4),
      explanation: z.string().min(1),
    })
  )
  .superRefine((q, ctx) => {
    const correctCount = q.options.filter((o) => o.isCorrect).length;
    if (correctCount !== 1) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: `Expected exactly 1 correct option, got ${correctCount}`,
        path: ["options"],
      });
    }
    // Unique-id check (story 9-7 review, P9). Duplicate ids cause React-key
    // warnings and scoring confusion downstream.
    const ids = new Set(q.options.map((o) => o.id));
    if (ids.size !== q.options.length) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: "Option ids must be unique",
        path: ["options"],
      });
    }
  });

