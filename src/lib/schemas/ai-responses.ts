/**
 * Micro-drill generation. Used by `generateMicroDrill` in error-tracker.
 * Generated by `gpt-4o` at temperature 0.5.
 */
export const microDrillQuestionSchema = z.object({
  question: z.string().min(1),
  options: z.array(z.string().min(1)).min(2),
  correctIndex: z.number().int().min(0),
  explanation: z.string().min(1),
});

export const microDrillSchema = z
  .object({
    title: z.string().min(1),
    explanation: z.string().min(1),
    questions: z.array(microDrillQuestionSchema).min(1),
    tip: z.string().min(1),
  })
  .superRefine((d, ctx) => {
    for (let i = 0; i < d.questions.length; i++) {
      const q = d.questions[i];
      if (q.correctIndex >= q.options.length) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message: `correctIndex ${q.correctIndex} out of range for ${q.options.length} options`,
          path: ["questions", i, "correctIndex"],
        });
      }
    }
  });

